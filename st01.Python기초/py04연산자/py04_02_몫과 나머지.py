import math #수학용 함수 모듈을 import 몫만 모듈 있고 나머지는 따로 없음

# 몫과 나머지 구하기
num = 5 / 2
print ( num ) #출력값:2.5


# 1몫을 구하시오
quotient = 5 // 2
print( "몫은:" , quotient ) #출력값

# 1-2몫을 구하시오 math.floor()
quotient = math.floor(5)
print( "몫은:" , quotient) #출력값

# 나머지를 구하시오
remainder = 5 % 2
print( "나머지는:" , remainder) #출력값