
#################################
# range() 함수의 사용법에 대해서 배워본다.
#################################


# range(10)
r1 = range(10)
print("range(10):", r1)

# range(0, 10, 1)
r1 = range(0, 10, 1)
print("range(0, 10, 1):", r1)

# range(0, 10, 2)
r1 = range(0, 10, 2)
print("range(0, 10, 2):", r1)

# range(10, 0, -1)
r1 = range(10, 0, -1)
print("range(10, 0, -1)", r1)

